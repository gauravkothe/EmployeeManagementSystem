package employee.management.system.pojo;

public class Address {
	int pin;
	String city;
	String area;
	String landmark;
	String state;
	String country;

	void setPin(int pin) {
		this.pin = pin;
	}

	int getPin() {
		return pin;
	}

	void setCity(String city) {
		this.city = city;
	}

	String getCity() {
		return city;
	}

	void setArea(String area) {
		this.area = area;
	}

	String getArea() {
		return area;
	}

	void setLandmark(String landmark) {
		this.landmark = landmark;
	}

	String getLandmark() {
		return landmark;
	}

	void setState(String state) {
		this.state = state;
	}

	String getState() {
		return state;
	}

	void setCountry(String country) {
		this.country = country;
	}

	String getCountry() {
		return country;

	}
}
